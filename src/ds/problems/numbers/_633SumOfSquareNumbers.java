package ds.problems.numbers;
//https://leetcode.com/problems/sum-of-square-numbers/
//633. Sum of Square Numbers

public class _633SumOfSquareNumbers {

    public boolean judgeSquareSum(int c) {
        for (int i = 2; i * i <= c; i++) {
            int count = 0;
            if (c % i == 0) {
                while (c % i == 0) {
                    count++;
                    c /= i;
                }
                if (i % 4 == 3 && count % 2 != 0)
                    return false;
            }
        }
        return c % 4 != 3;
    }

}
